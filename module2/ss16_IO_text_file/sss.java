public class sss {
    private int sss;

    public sss(int sss) {
        this.sss = sss;
    }

    public int getSss() {
        return sss;
    }

    public void setSss(int sss) {
        this.sss = sss;
    }

    @Override
    public String toString() {
        return "sss{" +
                "sss=" + sss +
                '}';
    }
}
